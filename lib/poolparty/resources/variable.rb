module PoolParty    
  module Resources
        
    class Variable < Resource
      
      default_options({
        :name => "var",
        :value => ""
      })
      
      def to_string(pre="\t")
        "$#{name} = #{value_string}"
      end
            
      def duplicatable?
        false
      end
      
      def value_string
        value.to_option_string
        # case value.class.to_s
        # when "Array"
        #   "[ #{value.map{|a| "'#{a}'"}.join(", ")} ]"
        # else
        #   "'#{value}'"
        # end
      end
      
    end
    
  end
end